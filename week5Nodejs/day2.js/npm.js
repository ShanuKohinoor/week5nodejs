//               NPM(Node Package Manager)

//         * Default pack manager for Node.js
//         * Used to download package into the project.
//         * Allows to install and use  third-party npm libraries in our code.
//         * How to install??
//                   - Must be in the folder. Path should be in the exact folder.
//                   - Terminal -> new terminal -> npm install package name
//                             * if we want locally(for that particular project only), we can write npm install package name. Eg:- npm install slug
//                             * if we want globally(in every project, use globally), we can write, npm install package name -g. Eg:- npm install slug -g

//         * Once we install npm, we can use it with the help of require.
//                   -Eg:- const slug = require ("slug")    
//                   - if we dont install slug, it will show error while running.

//         * Helps to:-
//              - Install packages from npm registry(NPM install)
//              - Manage versioning of packages(via package.json)  
//              - Publish your own packages(npm publish)
//         * Purpose :- Manage dependencies and scripts
//         * Use:- Every professional Node.js project





